package com.example.servingwebcontent;

public class AdminStats {

    private String DownloaderS;
    private String QueueS;
    private String SB1S;
    private String SB2S;
    private String SB3S;

    public AdminStats() {
    }

    public String getDownloaderS() {
        return DownloaderS;
    }

    public void setDownloaderS(String downloaderS) {
        DownloaderS = downloaderS;
    }

    public String getQueueS() {
        return QueueS;
    }

    public void setQueueS(String queueS) {
        QueueS = queueS;
    }

    public String getSB1S() {
        return SB1S;
    }

    public void setSB1S(String SB1S) {
        this.SB1S = SB1S;
    }

    public String getSB2S() {
        return SB2S;
    }

    public void setSB2S(String SB2S) {
        this.SB2S = SB2S;
    }

    public String getSB3S() {
        return SB3S;
    }

    public void setSB3S(String SB3S) {
        this.SB3S = SB3S;
    }
}
